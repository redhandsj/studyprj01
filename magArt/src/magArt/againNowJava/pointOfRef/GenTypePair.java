package magArt.againNowJava.pointOfRef;

/**
 * 総称型によるPairクラス
 *
 */
public class GenTypePair<L, R> {
	//=======================================================================
	// var
	//=======================================================================
	private L l;
	private R r;
	//=======================================================================
	// method
	//=======================================================================
	public GenTypePair(L l, R r){
		this.l = l;
		this.r = r;
	}
	//=======================================================================
	// getter / setter
	//=======================================================================
	public L getLeft(){
		return l;
	}

	public R getRight(){ return r; }
}
